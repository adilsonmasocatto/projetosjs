// uso do For
for(let i = 0; i < 10; i=i+1){
    console.log (`A soma de i + 1 é: ${i+1}`);
}

// uso do for incrementação resumida
for(let i = 0; i < 10; i++){
    console.log (`I é: ${i+1}`);
}

// uso do for decrementação resumida
for(let i = 10; i > 0; i--){
    console.log (`Agora I é: ${i}`);
}